    private void btnSalvarCadCidadesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarCadCidadesActionPerformed
           
            Document doc = new Document();
        try {
            PdfWriter.getInstance(doc, new FileOutputStream("C:/Users/Public/Cidades.pdf"));
            doc.open();
            doc.add(new Paragraph("CADASTRO DE CIDADES"));
            doc.add(new Paragraph(" "));
            doc.add(new Paragraph("C贸digo da cidade: " + txtCodDaCidade.getText()));
            doc.add(new Paragraph("Nome da cidade: " + txtNomeDaCidade.getText()));
            doc.add(new Paragraph("C贸digo do IBGE: " + txtCodDoIBGE.getText()));
            doc.add(new Paragraph("UF: " + txtUF.getText()));
            doc.add(new Paragraph("Nome do estado: " + txtNomeDoEstado.getText()));
            doc.close();
            JOptionPane.showMessageDialog(null, "Cadastro da cidade salvo com sucesso");
        } catch (DocumentException | FileNotFoundException ex) {
            Logger.getLogger(SCICadCidades.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnSalvarCadCidadesActionPerformed

